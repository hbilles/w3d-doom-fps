#!/usr/bin/env node
/**
 * Process raw AI-generated environment textures into game-ready tiles.
 *
 * For each entry in the config:
 *   - resize (squash) to a square tile with nearest-neighbor sampling
 *   - optionally hue-rotate (used to derive neon color variants from one master)
 *   - optionally emit an emissive mask PNG (bright pixels kept, rest black)
 *
 * Also writes a TypeScript manifest so TextureManager knows which file-based
 * textures exist without runtime probing.
 *
 * Usage: node tools/process-textures.mjs tools/atlas-configs/textures.json
 */

import sharp from 'sharp';
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

const configPath = process.argv[2] ?? 'tools/atlas-configs/textures.json';
const config = JSON.parse(await readFile(configPath, 'utf-8'));

const size = config.size ?? 256;
await mkdir(config.outputDir, { recursive: true });

/** Luminance threshold (0-255) above which a pixel counts as emissive. */
const EMISSIVE_THRESHOLD = 150;

const manifestEntries = [];

for (const tex of config.textures) {
  let img = sharp(tex.input).resize(size, size, { fit: 'fill', kernel: 'nearest' });

  if (tex.hueRotate || tex.brighten) {
    img = img.modulate({
      ...(tex.hueRotate ? { hue: tex.hueRotate } : {}),
      ...(tex.brighten ? { brightness: tex.brighten } : {}),
    });
  }

  const outPath = path.join(config.outputDir, `${tex.name}.png`);
  const { data, info } = await img
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  await sharp(data, { raw: { width: info.width, height: info.height, channels: 4 } })
    .png({ palette: true, colors: 64 })
    .toFile(outPath);

  if (tex.emissive) {
    // Keep only bright pixels, everything else black. If emissiveTint is set,
    // recolor emissive pixels with that tint scaled by luminance — source art
    // tube cores are near-white, which would otherwise bloom to pure white.
    const tint = tex.emissiveTint
      ? [1, 3, 5].map((o) => parseInt(tex.emissiveTint.slice(o, o + 2), 16))
      : null;
    const mask = Buffer.from(data);
    for (let i = 0; i < mask.length; i += 4) {
      const lum = 0.2126 * mask[i] + 0.7152 * mask[i + 1] + 0.0722 * mask[i + 2];
      if (lum < EMISSIVE_THRESHOLD) {
        mask[i] = 0;
        mask[i + 1] = 0;
        mask[i + 2] = 0;
      } else if (tint) {
        const t = lum / 255;
        mask[i] = Math.round(tint[0] * t);
        mask[i + 1] = Math.round(tint[1] * t);
        mask[i + 2] = Math.round(tint[2] * t);
      }
      mask[i + 3] = 255;
    }
    const emissivePath = path.join(config.outputDir, `${tex.name}_emissive.png`);
    await sharp(mask, { raw: { width: info.width, height: info.height, channels: 4 } })
      .png({ palette: true, colors: 64 })
      .toFile(emissivePath);
  }

  manifestEntries.push(`  ${JSON.stringify(tex.name)}: { emissive: ${Boolean(tex.emissive)} },`);
  console.log(`Processed ${tex.name}${tex.hueRotate ? ` (hue ${tex.hueRotate})` : ''}${tex.emissive ? ' +emissive' : ''}`);
}

const manifestSrc = `// AUTO-GENERATED by tools/process-textures.mjs — do not edit by hand.

/** File-based environment textures available under /assets/textures/. */
export const TEXTURE_MANIFEST: Record<string, { emissive: boolean }> = {
${manifestEntries.join('\n')}
};
`;

await writeFile(config.manifest, manifestSrc);
console.log(`Manifest written: ${config.manifest} (${manifestEntries.length} textures)`);
